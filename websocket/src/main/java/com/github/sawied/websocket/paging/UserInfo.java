package com.github.sawied.websocket.paging;

public class UserInfo {
	
	
	private Long id =null;
	
	private String name =null;
	

	public UserInfo() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
	
}
